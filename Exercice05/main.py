def sum(a: int, b: int) -> int:
    """
    adds up the two input values and return the sum

    :param a: the first value to be added
    :type a: int
    :param b:  the second value to be added
    :type b: int
    :return: the sum of the two values
    :rtype: int
    """
    return a + b


def subtraction(a: int, b: int) -> int:
    """
    subtract two members and returns the difference

    :param a: the first member
    :type a: int
    :param b: the second member to be subtracted
    :type b: int
    :return: the difference between the two members
    :rtype: int
    """

    return a - b
